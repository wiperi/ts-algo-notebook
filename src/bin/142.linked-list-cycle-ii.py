# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def detectCycle(self, head: Optional[ListNode]) -> Optional[ListNode]:
        vis = set()

        p = head
        while p:
            if p in vis:
                return p
            vis.add(p)
            p = p.next

        return None
        
